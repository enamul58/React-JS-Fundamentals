function add(num1, num2) {
  const output = num1 + num2;
  return output;
}
function sum(num1, num2) {
  const output = num1 - num2;
  return output;
}
function multiplication(num1, num2) {
  const output = num1 * num2;
  return output;
}
function division(num1, num2) {
  const output = num1 / num2;
  return output;
}

export { add, sum, multiplication, division };
